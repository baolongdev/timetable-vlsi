/** Pattern tiết `-------89012----` → { start, end } (vị trí i = tiết i+1) */
function parsePeriodPattern(pattern: string): {
  start: number
  end: number
} {
  let start = 0
  let end = 0
  for (let i = 0; i < pattern.length; i++) {
    if (pattern[i] !== "-") {
      if (start === 0) start = i + 1
      end = i + 1
    }
  }
  return { start, end }
}

/** Pattern tuần `1234567-90123456---------` → "1–7, 9–16" */
export function parseWeeksPattern(pattern: string): string {
  const weeks: number[] = []
  for (let i = 0; i < pattern.length; i++) {
    if (pattern[i] !== "-") weeks.push(i + 1)
  }
  if (weeks.length === 0) return "—"

  const ranges: string[] = []
  let rangeStart = weeks[0]
  let prev = weeks[0]
  for (let i = 1; i <= weeks.length; i++) {
    const w = weeks[i]
    if (w !== prev + 1) {
      ranges.push(
        rangeStart === prev ? String(rangeStart) : `${rangeStart}–${prev}`
      )
      rangeStart = w
    }
    prev = w
  }
  return ranges.join(", ")
}

export const sections: CourseSection[] = RAW.split("\n").map((line) => {
  const [day, periodPattern, code, group, capacity, room, weeksPattern, lang] =
    line.split("|")
  const { start, end } = parsePeriodPattern(periodPattern)
  return {
    code,
    courseName: SECTION_COURSE_NAMES[code] ?? code,
    group,
    day: Number(day),
    startPeriod: start,
    endPeriod: end,
    capacity: Number(capacity),
    room,
    weeksLabel: parseWeeksPattern(weeksPattern),
    language: lang as SectionLanguage,
  }
})

/**
 * Nhóm lớp của một môn — khớp theo mã môn hoặc theo tên
 * (để CO1023 "Hệ thống số" gom luôn CO1024 "Hệ thống số (TN)").
 */
export function getSectionsForCourse(
  code: string,
  name: string,
  data: CourseSection[] = sections
): CourseSection[] {
  const nameLower = name.trim().toLowerCase()
  return data.filter(
    (s) =>
      s.code === code ||
      s.courseName.toLowerCase().startsWith(nameLower)
  )
}

/** Nhãn thứ: 2–7 → "Thứ N", 8 → "CN" */
export function getSectionDayLabel(day: number): string {
  return day === 8 ? "CN" : `Thứ ${day}`
}
